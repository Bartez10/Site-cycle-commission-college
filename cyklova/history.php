<p class="textsize" style="text-align: justify; margin-left: 15px; margin-right: 15px;">Спеціальність чотири рази (2002р., 2006р., 2011р.2015р.) успішно пройшла акредитацію. Акредитаційною комісією відзначено високий технічно-організаційний рівень забезпечення та якість підготовки фахівців, і двічі (2006р., 2011р.) рекомендовано ліцензувати підготовку бакалаврів.</p>
<p class="textsize" style="text-align: justify; margin-left: 15px; margin-right: 15px;">Цикловою комісією програмування підготовлено 533 фахівці, з них 98 отримали дипломи з відзнакою. Ми пишаємося досягненнями своїх випускників. Андрій Біленький (2002 р.в.) та Чалінський Р.І (2013 р.в.)– зараз викладачі фахових дисциплін циклової комісії програмування, Хаурдінов Микола (2002 р.в.) – Python-разробник,  Демчук Віктор (2002 р.в.), Цільо Сергій (2004 р.в) – PHP-програмісти (Frontier Web Development), Дмитро Сокальський, Решетило Олександр, Олег Варшавський (2002 р.в.), – системні адміністратори мережі магазинів «Епіцентр», Гнатюк Роман (2005 р.в.) – м. Львів,  Ciklum Senior Ruby Developer, Дубина Віталій (2014 р.в) – м.Львів,  Ciklum, DevOps Enginner, Стельмащук Володимир (2016 р.в.) –  PMLAD, Anroid developer  і ще багато-багато інших. </p>
<p class="textsize" style="text-align: justify; margin-left: 15px; margin-right: 15px;">Науково-дослідницькі проекти, дипломні роботи старшокурсників — це надійний шлях пізнання кожним юним дослідником своїх творчих можливостей. Кращі роботи студентів представляються на обласних конференціях, де вони неодноразово займали призові місця і мають практичне застосування у житті навчального закладу. Серед них: «АРМ завуча школи» (Пріян Наталія, 2009р.),  «Автоматизована  інформаційна система рейтинового розподілу абітурієнтів» (Дубина Віталій, 2014 р.), «Інформаційна система моніторингу доступності та дієздатності серверів» (Галябарда Максим, 2014), «Автоматизована  інформаційна система «Електронний журнал навчальної групи» (Біланюк Святослав, 2015 р.),  «Розробка інформаційної системи надання кредитів»  (Гайдук Роман, 2016 р), «Розробка електронного органайзера викладача»  (Євсюков Владислав, 2017 р), «Автоматизована інформаційна система обліку населення у відділі реєстрації Городоцької ОТГ» (Береза Юрій, 2018 р.), «Розробка сайту молодіжної організації із створенням інтерактивної карти проблем об’єднаної територіальної громади»  (Кравчик Вадим, 2018 р.), «Створення телеграм боту для бронювання готельних номерів» (Борніцький Ростислав, 2019 р.), «Розробка Web-сайту циклової комісії програмування» (Бортняк Андрій, 2020 р.), «Створення сайту послуг для інтернет провайдера» (Павлов Михайло, 2020 р.), «Розробка Web-сайту Гусятинської філії Малої академії наук України» (Романський Степан, 2020 р.).</p>
<p class="textsize" style="text-align: justify; margin-left: 15px; margin-right: 15px;">Особливою сторінкою в роботі циклової комісії з 2014 року є  відкриття секції «Інформаційні технології» Малої академії наук України. У січні 2016 року студенти першого курсу представляли свої роботи на обласному конкурсі – захисті науково-дослідницьких робіт і отримали високу оцінку. Диплом першого ступеня та право представляти  Тернопільську область на ІІІ-му етапі конкурсу  у м. Києві одержав Олянін Денис (гр. 11) з роботою «Розробка Android-додатку «Розгадай ребус» , диплом другого ступеня отримав Клекот Віталій (гр. 11) з роботою «Демонстрація основних математичних операцій над матрицями  в середовищі Delphi». У  2017 році робота Оляніна Дениса «Створення мобільного додатку для розвитку логічного мислення молодшого та середнього шкільного віку» здобула  диплом І-го ступеня в обласному конкурсі і була представлена у ІІІ- му етапі у м. Київ. Цікаві роботи варто відмітити за 2019 рік: Бесащук Микола  «Розробка сайту підтримки волонтерського руху для відновлення храму св. Антонія»  та Бульчак Марія «Розробка смарт-одягу для безпеки учасників дорожнього руху». А роботи з математики  Шинкар Ірини «Деякі методи інтерполювання функції» та  Остапишин Лілії «Методи розв’язування задач динамічного і сіткового програмування» здобули диплом І-го ступеня на обласному етапі. У 2020 році високо оцінені членам журі обласного конкурсу такі роботи: Машталяр Віктор «Розробка веб-застосування для моніторингу наповненості сміттєвих контейнерів» та Бульчак Марія «Автоматизована система моніторингу наповненості контейнерів для твердих побутових відходів». Ці роботи будуть представлені на ІІІ- му етапі у м. Київ.</p>

