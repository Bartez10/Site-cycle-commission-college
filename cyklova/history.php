<p class="textsize" style="text-align: justify; margin-left: 15px; margin-right: 15px;">Починаючи з 2011 року, Гусятинський коледж виставляє команди студентів для участі в 1/8 фіналу Міжнародної олімпіади з програмування і постійно демонструє найвищі результати серед команд ВНЗ І-ІІ р.а. Тернопільської області. В 2014 та 2015  роках команди у складі Чорного Ігоря, Дубини Віталія, Стельмащука Володимира та Біланюка Святослава, Давидова Андрія, Мех Оксани вийшли у наступний тур Міжнародної олімпіади з програмування і брали участь у її роботі в м. Львові. У 2017 році дві команди коледжу у складі  Борніцького  Ростислава, Клекота Віталія, Зеленського Костянтина та Кузя Сергія, Сави Романа, Шинкара Андрія виборовши І-е та ІІІ-є місце у ІІ-му турі відповідно серед команд Південно-Західного  регіону, брали участь  у заключному етапі Всеукраїнської олімпіади з програмування у м. Одеса. 2018 рік приніс вагомий здобуток: команда у складі Борніцького Ростислава, Сарновського Володимира, Романченка Валерія здобула диплом ІІ-го ступення серед коледжів України у заключному ІІІ-му етапі Всеукраїнської олімпіади з програмування (ІІ-га ліга).</p>
